/**
 * The priority of a service.
 */
public enum ServicePriority {

    /**
     * The lowest priority
     */
    LOWEST,

    /**
     * The second lowest priority
     */
    LOW,

    /**
     * Normal priority
     */
    NORMAL,

    /**
     * The second highest priority
     */
    HIGH,

    /**
     * The highest of importance!
     */
    HIGHEST;
}
